#ifndef FITOBJ_H
#define FITOBJ_H

#include <stddef.h>
#include "RooFitResult.h"

class FitResult
{
    public:
        double weight;
        double weightError;
        RooFitResult* rooFitResult;


    FitResult() :
        weight(0),
        weightError(0),
        rooFitResult(NULL)
    {}

    ~FitResult(){
        if(rooFitResult != NULL){
           delete rooFitResult;
        }
    }
};


#endif
